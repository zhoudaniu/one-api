// GetData 获取数据看板数据（管理员）
func GetData(c *gin.Context) {
	_ = c.Query("username") // username 参数暂未使用
	startTimestamp, _ := strconv.ParseInt(c.Query("start_timestamp"), 10, 64)
	endTimestamp, _ := strconv.ParseInt(c.Query("end_timestamp"), 10, 64)

	// 获取所有用户的日志统计数据
	logs, err := model.SearchLogsByDayAndModel(0, int(startTimestamp), int(endTimestamp))
	if err != nil {
		// 如果查询失败，返回空数据
		logs = []*model.LogStatistic{}
	}

	// 转换为前端需要的格式
	data := make([]map[string]interface{}, 0)
	for _, log := range logs {
		item := map[string]interface{}{
			"count":      log.RequestCount,
			"model_name": log.ModelName,
			"quota":      log.Quota,
			"created_at": startTimestamp, // 使用起始时间作为时间戳
		}
		data = append(data, item)
	}

	// 如果没有数据，返回示例数据
	if len(data) == 0 {
		data = []map[string]interface{}{
			{
				"count":      0,
				"model_name": "无数据",
				"quota":      0,
				"created_at": startTimestamp,
			},
		}
	}

	c.JSON(http.StatusOK, gin.H{
		"success": true,
		"message": "",
		"data":    data,
	})
	return
}

